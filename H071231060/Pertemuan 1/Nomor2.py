Celcius = float(input("masukkan nilai C="))

Fahrenheit = 9/5*Celcius+32
print(f"hasil konversi dari suhu {Celcius} derajat celsius ke fahrenheit adalah {Fahrenheit} F")
Reamur = 4/5*Celcius
print(f"hasil konversi dari suhu {Celcius} derajat celsius ke Reamur adalah {Reamur} R")
Kelvin = Celcius+437
print(f"hasil konversi dari suhu {Celcius} derajat celsius ke Kelvin adalah {Kelvin} K")